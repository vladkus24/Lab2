# Задача №1
# 
#   Дано ціле число n (1 ≤ n ≤ 4), яке визначає пору року: 
#   весна, літо, осінь, зима. За вказаним значенням n необхідно
#   надрукувати перелік місяців, які відносяться до цієї пори року.
# 
# Дзюба Владислав

n = int(input("Введіть ціле число: "))

if n == 1:
    print("Весна")
elif n == 2:
    print("Літо")
elif n == 3:
    print("Осінь")
elif n == 4:
    print("Зима")
else:
    print("Введіть число від 1 до 4")
